package week02_1_java_basic;

public class Rectangle2 {

	private double width;
	private double height;
	
	public Rectangle2(double width, double height) {
		this.width = width;
		this.height = height;
	}
	
	public double getHeight() {
		return this.height;
	}
	
	public double getWidth() {
		return this.width;
	}
}
